# Variáveis 
numero1 = 0
numero2 = 0
resultado = 0
operacao = ''

# Entradas dos numeros a serem usados
numero1 = int(input('Digite o primeiro numero: \t'))
numero2 = int(input('Digite o segundo numero: \t'))

# Antes da entrada da operação, mostrar a lista de operações
print ('Lista de operações: +, -, /, x')
operacao = input('Digite a operação desejada: \n\t')

# Condições para que seja realizada cada operação
if operacao == ('+'):
  resultado = numero1 + numero2
  print ("Este é o resultado: ", (resultado))
elif operacao == ('-'):
  resultado = numero1 + numero2
  print ("Este é o resultado: ", (resultado))
elif operacao == ('/'):
  resultado = numero1 / numero2
  print ("Este é o resultado: ", (resultado))
elif operacao ==('x'):
  resultado = numero1*numero2
  print ("Este é o resultado: ", (resultado))
#Caso o usuario não coloque um valor válido
else:
  print("Operação inválida, tente novamente!")


